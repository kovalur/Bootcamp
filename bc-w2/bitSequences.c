#include <stdio.h>

int main() {
    int length;
    int counter = 1;
    
    scanf("%d", &length);
    
    for ( int i = length; i > 1; i -= 2 ) {
        printf("i=%d\n", i);
        for ( int j = 1; j < i - 1; j++ ) {
            counter += 1;
            printf("j=%d\n", j);
        }  
    }
    
    if ( length == 1 ) {
        counter += 1;
    } else {
        counter += 2;
    }
    
    printf("%d\n", counter);
    return 0;
}

